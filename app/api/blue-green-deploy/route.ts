import { NextResponse } from "next/server";
import OpenAI from "openai";

export const runtime = "nodejs";

export async function GET() {
  return NextResponse.json({
    description:
      "Blue/green deployment config generator. POST { service: string, provider: string } to generate a blue/green deployment configuration.",
  });
}

export async function POST(req: Request) {
  try {
    if (!process.env.OPENAI_API_KEY) {
      return NextResponse.json({ error: "Missing OPENAI_API_KEY" }, { status: 500 });
    }
    const body = (await req.json().catch(() => ({}))) as Record<string, unknown>;
    const client = new OpenAI({ apiKey: process.env.OPENAI_API_KEY });
    const completion = await client.chat.completions.create({
      model: "gpt-4o",
      messages: [
        {
          role: "system",
          content:
            "You are a DevOps architect. Generate a blue/green deployment configuration for the given service and cloud provider. Include environment definitions, load balancer switch-over steps, smoke test hooks, rollback procedure, and DNS/traffic cutover timing. Tailor the configuration to the specified provider (e.g., AWS CodeDeploy, GCP Cloud Deploy, Azure Deployment Slots, Kubernetes). Return a structured YAML or JSON configuration with inline documentation.",
        },
        { role: "user", content: JSON.stringify(body) },
      ],
    });
    const result = completion.choices[0]?.message?.content ?? "";
    return NextResponse.json({ result });
  } catch (err: unknown) {
    const message = err instanceof Error ? err.message : "Server error";
    return NextResponse.json({ error: message }, { status: 500 });
  }
}
